def seed_admin(app):
    from .models import User
    from .extensions import db

    with app.app_context():
        if not User.query.filter_by(username="admin").first():
            admin_login = current_app.config.get("ADMIN_LOGIN") or os.getenv("ADMIN_LOGIN")
            admin_password = current_app.config.get("ADMIN_PASSWORD") or os.getenv("ADMIN_PASSWORD")

            admin = User(username=admin_login, password=generate_password_hash(admin_password), is_admin=True)
            db.session.add(admin)
            db.session.commit()

